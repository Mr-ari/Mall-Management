    public void show_product_details() {
       
        pid.setText(String.valueOf(p.getProduct_id()));
        pname.setText(p.getProduct_name());
        pqty.setText(String.valueOf(p.getAvailable_qty()));
        pdis.setText(String.valueOf(p.getDiscount()));
        pthres.setText(String.valueOf(p.getThreshold_qty()));
        pprice.setText(String.valueOf(p.getPrice()));
        pret.setText(String.valueOf(p.getReturn_period()));
        
        pvendor.setText("--");
        vendors.forEach((Vendor v) -> {
            String prev = pvendor.getText();
            pvendor.setText(prev+v.getVendor_name()+" -- ");
        });
        
    }

    private String fields_check() {
        String result="";
        try{
            Integer valueOf = Integer.valueOf(pqty.getText());
            if(valueOf < 0) result += "Quantity";
        }catch(NumberFormatException ex){
            result += "Quantity ";
        }
        try{
            Integer valueOf = Integer.valueOf(pthres.getText());
            if(valueOf < 0) result += "Threshold-qty ";
        }catch(NumberFormatException ex){
            result += "Threshold-qty ";
        }
        try{
            Integer valueOf = Integer.valueOf(pret.getText());
            if(valueOf < 0) result += "Return-Period ";
        }catch(NumberFormatException ex){
            result += "Return-Period ";
        }
        try{
            Double valueOf = Double.valueOf(pprice.getText());
            if(valueOf < 0) result += "Price ";
        }catch(NumberFormatException ex){
            result += "price ";
        }
        try{
            Double valueOf = Double.valueOf(pdis.getText());
            if(valueOf >= 100 || valueOf<0) result += "Discount ";
        }catch(NumberFormatException ex){
            result += "Discount ";
        }
        
        return result;
    }
