    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String fc = fields_check();
        
        if(fc.isEmpty()){
            
            int op = JOptionPane.showConfirmDialog(this, "Update Item ?");
            if(op == 1 || op == 2) return;
            
            try{
            Class.forName("com.mysql.jdbc.Driver");
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mall_management","root","ari1106")) {
                PreparedStatement ps_name = con.prepareStatement("update product_details set product_name=? where product_id=?");
                ps_name.setString(1, pname.getText());
                ps_name.setInt(2, prod_id);
                ps_name.execute();
                
                PreparedStatement ps_price = con.prepareStatement("update product_details set price=? where product_id=?");
                ps_price.setDouble(1,Double.valueOf(pprice.getText()));
                ps_price.setInt(2, prod_id);
                ps_price.execute();
                
                PreparedStatement ps_quantity = con.prepareStatement("update product_details set available_qty=? where product_id=?");
                ps_quantity.setInt(1, Integer.valueOf(pqty.getText()));
                ps_quantity.setInt(2, prod_id);
                ps_quantity.execute();
                
                PreparedStatement ps_ret = con.prepareStatement("update product_details set return_period=? where product_id=?");
                ps_ret.setInt(1, Integer.valueOf(pret.getText()));
                ps_ret.setInt(2, prod_id);
                ps_ret.execute();
                
                PreparedStatement ps_dis = con.prepareStatement("update product_details set product_discount=? where product_id=?");
                ps_dis.setDouble(1,Double.valueOf(pdis.getText()));
                ps_dis.setInt(2, prod_id);
                ps_dis.execute();
                
                PreparedStatement ps_thres = con.prepareStatement("update product_details set qty_threshold=? where product_id=?");
                ps_ret.setInt(1, Integer.valueOf(pthres.getText()));
                ps_ret.setInt(2, prod_id);
                ps_ret.execute();
                
                PreparedStatement get_v = con.prepareStatement("select * from vendor_product_details where product_id=?");
                get_v.setInt(1, Integer.valueOf(pid.getText()));
                
                ResultSet rs = get_v.executeQuery();
                List <Integer> v_ids = new ArrayList<>();
                while(rs.next()){
                    v_ids.add(rs.getInt("vendor_id"));
                }
                vendors.forEach((Vendor v) -> {
                    if(!v_ids.contains(v.getVendor_id())){
                        try {
                            PreparedStatement ps = con.prepareStatement("INSERT INTO vendor_product_details(product_id,vendor_id) VALUES(?,?)");
                            ps.setInt(1, Integer.valueOf(pid.getText()));
                            ps.setInt(2, v.getVendor_id());
                            ps.execute();
                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(null, ex.getMessage());
                        }
                    }
                });
                
                
                sm.refresh_table();
                this.dispose();
            }
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
            
        }
        else{
            StringTokenizer stz = new StringTokenizer(fc," "); 
            String dispFields = stz.nextToken();
            while(stz.hasMoreTokens()){
                dispFields += ", ";
                dispFields += stz.nextToken();
            }
       
            JOptionPane.showMessageDialog(this,dispFields + " fields have wrong values");
        }
        
    }//GEN-LAST:event_saveBtnActionPerformed

