    private void fetch_product_details(String searchItem) {
        searchItem += '%';
        DefaultTableModel table = (DefaultTableModel) prod_table.getModel();
        int sz = table.getRowCount();
        for(int i=0;i<sz;i++) table.removeRow(0);
        try{
            Class.forName("com.mysql.jdbc.Driver");
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mall_management","root","ari1106")) {
                PreparedStatement ps =  con.prepareStatement("Select * from product_details where product_name like ? order by product_name");
                ps.setString(1, searchItem);
                ResultSet rs = ps.executeQuery();
                List <Product> prod_list = new ArrayList<>();
                while(rs.next()){
                    prod_list.add(new Product(rs.getInt("product_id"),rs.getString("product_name"),rs.getInt("available_qty"),rs.getDouble("price"),rs.getInt("return_period"),rs.getDouble("product_discount"),rs.getInt("qty_threshold")));
                }
                
                if(prod_list.isEmpty()){
                    JOptionPane.showMessageDialog(this, "No item found");
                }
                
                prod_list.forEach((p) -> {
            table.addRow(new Object[] { p.getProduct_id(),p.getProduct_name(),p.getAvailable_qty(),p.getPrice(),p.getReturn_period(),p.getDiscount(),p.getThreshold_qty()});
        }); 
            }
        } catch (ClassNotFoundException  | SQLException ex) {
           JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

    public void refresh_table() {
        fetch_product_details((String) prod_name.getSelectedItem());
    }
