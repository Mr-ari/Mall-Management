    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        // TODO add your handling code here:
        
        int selected_row = prod_table.getSelectedRow();
        int prod_id = (int) prod_table.getValueAt(selected_row, 0);
        int op = JOptionPane.showConfirmDialog(this, "Are You Sure ? \n"+(String) prod_table.getValueAt(selected_row, 1)+" will be deleted ?");
        if(op == 1 || op == 2) return;
        
         try{
            Class.forName("com.mysql.jdbc.Driver");
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mall_management","root","ari1106")) {
                PreparedStatement ps =  con.prepareStatement("delete from product_details where product_id=?");
                ps.setInt(1, prod_id);
                ps.execute();
            }
        } catch (ClassNotFoundException  | SQLException ex) {
           JOptionPane.showMessageDialog(this, ex.getMessage());
        }
         
         refresh_table();
        
    }//GEN-LAST:event_delBtnActionPerformed

